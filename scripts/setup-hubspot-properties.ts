/**
 * One-shot, idempotent HubSpot property bootstrap.
 *
 *   npx tsx scripts/setup-hubspot-properties.ts          # dry-run (default)
 *   npx tsx scripts/setup-hubspot-properties.ts --apply  # actually create
 *
 * Reads existing properties via `GET /crm/v3/properties/{objectType}`; for
 * each property in the spec table below, prints one of:
 *
 *   ✓ exists      — already in HubSpot, no-op
 *   + created     — newly created (apply mode)
 *   ~ would create — would be created (dry-run)
 *   ✗ failed      — API rejected the create call (logged + counted)
 *
 * Designed to be safe to re-run any number of times — never modifies an
 * existing property (no PATCH calls), only creates missing ones. If a
 * property exists with a different type/options, leave it alone; sort out
 * mismatches in the HubSpot UI manually.
 *
 * Property spec sources:
 *   - docs/integrations/hubspot-integration-phase-0b-setup.md (originals)
 *   - docs/plans/post-launch-migration-pass-2-7.md           (BI cron set)
 *   - src/lib/bi/types.ts                                     (enum vocab)
 *
 * Auth: uses HUBSPOT_STATIC_TOKEN from .env.local (same as runtime code).
 * Required scopes (already on the LP app): crm.schemas.contacts.write,
 *   crm.schemas.deals.write, crm.schemas.companies.write,
 *   crm.schemas.custom.write (covers tickets).
 */
