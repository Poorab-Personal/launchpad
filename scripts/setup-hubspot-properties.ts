type ObjectType = 'contacts' | 'tickets' | 'deals' | 'companies';

type EnumOption = { label: string; value: string };

type PropSpec = {
  name: string;
  label: string;
  type: 'string' | 'number' | 'date' | 'datetime' | 'enumeration';
  fieldType: 'text' | 'textarea' | 'number' | 'date' | 'select';
  description: string;
  options?: EnumOption[];
};

// HubSpot default group names — same as what the UI uses.
const DEFAULT_GROUP: Record<ObjectType, string> = {
  contacts: 'contactinformation',
  tickets: 'ticketinformation',
  deals: 'dealinformation',
  companies: 'companyinformation',
};

const opts = (vals: readonly string[]): EnumOption[] =>
  vals.map((v) => ({ label: v, value: v }));

