function getClient(): Client {
  const token = process.env.HUBSPOT_STATIC_TOKEN;
  if (!token) {
    console.error('✗ HUBSPOT_STATIC_TOKEN is not set in .env.local');
    process.exit(1);
  }
  return new Client({ accessToken: token });
}

async function listExistingNames(
  hs: Client,
  objectType: ObjectType,
): Promise<Set<string>> {
  const result = await hs.crm.properties.coreApi.getAll(objectType);
  return new Set(result.results.map((p) => p.name));
}

async function processObject(hs: Client, objectType: ObjectType): Promise<{
  existing: number;
  created: number;
  wouldCreate: number;
  failed: number;
}> {
  const specs = SPEC[objectType];
  const tally = { existing: 0, created: 0, wouldCreate: 0, failed: 0 };

  console.log(`\n━━ ${objectType.toUpperCase()} (${specs.length} property/-ies) ━━`);

  let existing: Set<string>;
  try {
    existing = await listExistingNames(hs, objectType);
  } catch (err) {
    console.error(`  ✗ Failed to list ${objectType} properties:`, err instanceof Error ? err.message : err);
    return tally;
  }

  for (const spec of specs) {
    if (existing.has(spec.name)) {
      console.log(`  ✓ exists       ${spec.name}`);
      tally.existing++;
      continue;
    }

    if (!APPLY) {
      console.log(`  ~ would create ${spec.name}  (${spec.type}${spec.options ? `, ${spec.options.length} options` : ''})`);
      tally.wouldCreate++;
      continue;
    }

    try {
      await hs.crm.properties.coreApi.create(objectType, {
        name: spec.name,
        label: spec.label,
        type: spec.type as unknown as PropertyCreateTypeEnum,
        fieldType: spec.fieldType as unknown as PropertyCreateFieldTypeEnum,
        groupName: DEFAULT_GROUP[objectType],
        description: spec.description,
        ...(spec.options
          ? {
              options: spec.options.map((o, i) => ({
                label: o.label,
                value: o.value,
                displayOrder: i,
                hidden: false,
              })),
            }
          : {}),
      });
      console.log(`  + created      ${spec.name}`);
      tally.created++;
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err);
      console.log(`  ✗ failed       ${spec.name}  — ${msg}`);
      tally.failed++;
    }
  }

  return tally;
}

async function main(): Promise<void> {
  console.log(`HubSpot property bootstrap — mode: ${APPLY ? 'APPLY (will create missing)' : 'DRY-RUN (no writes)'}`);
  const hs = getClient();

  const grandTally = { existing: 0, created: 0, wouldCreate: 0, failed: 0 };

  for (const objectType of ['contacts', 'tickets', 'deals', 'companies'] as const) {
    const tally = await processObject(hs, objectType);
    grandTally.existing += tally.existing;
    grandTally.created += tally.created;
    grandTally.wouldCreate += tally.wouldCreate;
    grandTally.failed += tally.failed;
  }

  console.log(`\n━━ SUMMARY ━━`);
  console.log(`  existing:     ${grandTally.existing}`);
  if (APPLY) {
    console.log(`  created:      ${grandTally.created}`);
  } else {
    console.log(`  would create: ${grandTally.wouldCreate}`);
  }
  if (grandTally.failed > 0) {
    console.log(`  FAILED:       ${grandTally.failed}`);
    process.exit(1);
  }

  if (!APPLY && grandTally.wouldCreate > 0) {
    console.log(`\nRe-run with --apply to create the ${grandTally.wouldCreate} missing property/-ies.`);
  }
}

main().catch((err) => {
  console.error('Unhandled error:', err);
  process.exit(1);
});
